2.计分办法
(1)单人项目:每获得 1金(银、铜)牌，按照 1枚金银、铜)牌统计给运动员两次计分单位，每获得一个名次，按照所获名次相应分值 (13、11、10、9、8、7、6、分)统计给该计分单位。
(2)两人项目:每获得 1枚金(银、铜)牌，每人分别按照 0.5 枚金(银、铜)牌统计给运动员两次计分单位;每获得一个名次，每人分别按照所获名次相应分值的 50%(6.5、5.5 分5、4.5、4、3.5、3、2.5分) 统计给该计分单位。
(3)两人以上项目:两次计分单位只计分数，不计奖牌每获得一个名次，每人分别按照所获名次相应分值的 50%(6.55.5、5、4.5、4、3.5、3、2.5分) 统计给运动员两次计分单位。如同一两次计分单位在同一比赛小项上有两名或两名以上的计分运动员，则只能按照所获名次相应分值 (13、11、10、9、87、6、5分)统计给该计分单位。
(4)足球、篮球(不含三人制篮球 )、排球、橄榄球、棒垒球项目:两次计分单位只计分数，不计奖牌。每获得一个名次每人分别按照所获名次相应分值(13、11、10、9、8、7、6、52、1 分)统计给运动员两次计分单位。如同一两次计分单位在同一比赛小项上有四名或四名以上的计分运动员,则最多只能按照所获名次相应分值的4倍 (52、44、40、36、32、2824、20、16、12、8、4分) 统计给该计分单位。
(三)青少年组破纪录计分办法
打破重庆市最高纪录且被予以认定的，加计 13 分，破纪录加分直接计入单项成绩。举重项目抓举、挺举破重庆市最高纪录予以承认，但只按总成绩破重庆市最高纪录计算加分; 游泳接力分段个人成绩、射击、射箭团体项目个人成绩破纪录不加分。名运动员在一个项目中多次破纪录的，只按最高一次计算。
