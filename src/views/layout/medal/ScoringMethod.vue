(二)共建及市重点业余体校两次计分
1.计分要求
(1) 两次计分仅限于青少年组。
(2)市重点业余体校之间、联办单位之间、市重点业余体校和联办单位之间，同一布局(联办)项目的运动员不实行两次计分。
